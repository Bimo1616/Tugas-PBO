def hitung_luas( LuasSisi1,LuasSisi2,LuasSisi3,LuasSisi4):
     Ls1 = float(LuasSisi1)
     Ls2 = float(LuasSisi2)
     Ls3 = float(LuasSisi3)
     Ls4 = float(LuasSisi4)
     

     L = round (Ls1+Ls2+Ls3+Ls4) 
     return L
 

def hitung_volume(LuasAlas,Tinggi):  
     La = float(LuasAlas)
     t  = float(Tinggi)

     V = round ( 1/6*La*t)
     return V



